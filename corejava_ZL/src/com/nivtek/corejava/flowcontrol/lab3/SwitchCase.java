package com.nivtek.corejava.flowcontrol.lab3;

public class SwitchCase {

	public static void main(String[] args) {
		
         char var ='Z';
         
         switch(var) {
         case 'A':
        	 System.out.println("your grade is between 80-100");
        	 break;
         case 'B':
        	 System.out.println("your grade is between 73-79");
        	 break;
         case 'C':
        	 System.out.println("your grade is between 65-72");
        	 break;
         case 'D':
        	 System.out.println("your grade is between 55-64");
        	 break;
         case 'E':
        	 System.out.println("your grade is less than 55");
        	 break;
        default:
        	System.out.println("Grade Doesn't Exist");
        	break;
        	 
      
         
         
         
         
         
         }

		
		
		
	}

}
