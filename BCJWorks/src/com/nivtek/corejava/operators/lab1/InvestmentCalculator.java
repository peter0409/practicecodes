public class InvestmentCalculator {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		double investment = getInvestmentAfter3Yrs();
		System.out.println("Value of investment after 3 years is: "+ investment);
	}

	/**
	 * @return thirdYrValue the investment
	 */
	public static double getInvestmentAfter3Yrs() {
		int investment = 14000;
		double firstYrValue = 1.4 * investment;
		double secondYrValue = firstYrValue - 1500;
		double thirdYrValue = 1.12 * secondYrValue;
		return thirdYrValue;
	}

}
