package com.nivtek.corejava.operators.lab3;

public class DivisionAndModulusOperationsApp {

	public static void main(String[] args) {
		int a = 25;
		int b = 2;
		System.out.println("The 2 numbers are: " + a + ", " + b);
		double result = 25 / 2;
		double remainder = 25 % 2;
		System.out.println("The result of / and % operators is " + result + " \t" + remainder);

	}

}
